function getQuoteByDayOfYear(dayOfYear) {
  var keys = Object.keys(QUOTES);
  var catIdx = dayOfYear % keys.length;
  var cat = keys[catIdx];
  var quotes = QUOTES[cat];
  var quoteIdx = Math.floor(dayOfYear / keys.length) % quotes.length;
  return {
    text: quotes[quoteIdx],
    category: cat,
    categoryName: CATEGORIES[catIdx].name
  };
}

function getRandomQuote() {
  var keys = Object.keys(QUOTES);
  var cat = keys[Math.floor(Math.random() * keys.length)];
  var quotes = QUOTES[cat];
  var q = quotes[Math.floor(Math.random() * quotes.length)];
  var catInfo = CATEGORIES.filter(function(c) { return c.id === cat; })[0];
  return {
    text: q,
    category: cat,
    categoryName: catInfo ? catInfo.name : ''
  };
}

function getQuoteFromCategory(cat) {
  var quotes = QUOTES[cat];
  if (!quotes) return getRandomQuote();
  var q = quotes[Math.floor(Math.random() * quotes.length)];
  var catInfo = CATEGORIES.filter(function(c) { return c.id === cat; })[0];
  return {
    text: q,
    category: cat,
    categoryName: catInfo ? catInfo.name : ''
  };
}

function getDayOfYear() {
  var now = new Date();
  var start = new Date(now.getFullYear(), 0, 0);
  var diff = now - start;
  var oneDay = 1000 * 60 * 60 * 24;
  return Math.floor(diff / oneDay);
}


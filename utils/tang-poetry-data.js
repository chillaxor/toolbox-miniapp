/**
 * 获取所有分类
 */
function getCategories() {
  var cats = [];
  var seen = {};
  for (var i = 0; i < POEMS.length; i++) {
    var cat = POEMS[i].category;
    if (!seen[cat]) {
      seen[cat] = true;
      cats.push(cat);
    }
  }
  return cats;
}

/**
 * 根据分类获取诗列表
 */
function getPoemsByCategory(category) {
  if (!category || category === '全部') {
    return POEMS;
  }
  return POEMS.filter(function (p) {
    return p.category === category;
  });
}

/**
 * 根据标题获取诗
 */
function getPoemByTitle(title) {
  for (var i = 0; i < POEMS.length; i++) {
    if (POEMS[i].title === title) {
      return POEMS[i];
    }
  }
  return null;
}

module.exports = {
  POEMS: POEMS,
  getCategories: getCategories,
  getPoemsByCategory: getPoemsByCategory,
  getPoemByTitle: getPoemByTitle
};

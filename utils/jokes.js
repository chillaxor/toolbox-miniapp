/**
 * 获取所有分类
 */
function getCategories() {
  return Object.keys(JOKES_DATA);
}

/**
 * 随机获取一条笑话
 * @param {string} category - 分类，不传则从所有分类随机
 */
function getRandomJoke(category) {
  var categories;
  if (category && JOKES_DATA[category]) {
    categories = [category];
  } else {
    categories = Object.keys(JOKES_DATA);
  }

  var cat = categories[Math.floor(Math.random() * categories.length)];
  var jokes = JOKES_DATA[cat];
  var joke = jokes[Math.floor(Math.random() * jokes.length)];

  return { content: joke, category: cat };
}

/**
 * 获取某分类的笑话数量
 */
function getJokeCount(category) {
  if (!category) {
    var total = 0;
    var keys = Object.keys(JOKES_DATA);
    for (var i = 0; i < keys.length; i++) {
      total += JOKES_DATA[keys[i]].length;
    }
    return total;
  }
  return JOKES_DATA[category] ? JOKES_DATA[category].length : 0;
}

module.exports = {
  getCategories: getCategories,
  getRandomJoke: getRandomJoke,
  getJokeCount: getJokeCount
};
